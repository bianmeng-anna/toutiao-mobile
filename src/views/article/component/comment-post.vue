<template>
  <div class="comment-post">
    <van-field
      class="post-field"
      v-model.trim="message"
      rows="2"
      autosize
      type="textarea"
      maxlength="50"
      placeholder="请输入留言"
      show-word-limit
    />
    <van-button
      class="post-btn"
      @click="onpost"
      :disabled="message.length===0"
    >发布</van-button>
  </div>
</template>

<script>
import { addComment } from '@/api/comment'
export default {
  name: 'CommentPost',
  components: {},
  // inject: ['articleId'],
  inject: {
    articleId: {
      type: [Number, Object, String],
      default: null
    }
  },
  props: {
    target: {
      type: [Number, Object, String],
      required: true
    }
  },
  data () {
    return {
      message: ''
    }
  },
  computed: {},
  watch: {},
  created () {},
  mounted () {},
  methods: {
    async onpost () {
      try {
        this.$toast.loading({
          message: '发布中...',
          forbidClick: true
        })
        const { data } = await addComment({
          target: this.target.toString(), // 评论的目标id（评论文章即为文章id，对评论进行回复则为评论id）
          content: this.message, // 评论内容
          art_id: this.articleId ? this.articleId.toString() : this.articleId
        })
        this.$emit('post-success', data.data)
        console.log(data)
        this.$toast.success('发布成功')
      } catch (err) {
        this.$toast.fail('操作失败')
      }
    }
  }
}
</script>

